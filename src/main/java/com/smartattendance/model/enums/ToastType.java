package com.smartattendance.model.enums;

/**
 * Represents the type of toast notification shown in the application.
 *
 * Each type indicates the visual style and purpose of the message
 * displayed to the user.
 *
 * @author Chue Wan Yan
 *
 * @version 15:11 07 Nov 2025
 */
public enum ToastType {
    SUCCESS, // Indicates a successful operation
    ERROR, // Indicates an error occurs in operation
    WARNING // Indicates a warning message
}